package com.xworkz.java.variables;

public class Narrowing {

	public static void main(String[] args) {
		
		float f= 4000.50f;
		
		int i=(int)f;
		
		System.out.println(f);
		System.out.println(i);
		
		double d=3000.80;
		short s=(short)d;
		System.out.println(d);
		System.out.println();
		
	}
}
